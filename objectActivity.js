// Activity 3: Object Manipulation in JavaScript

// Define an object named car with initial properties
let car = {
    type: "Sedan",
    model: "Civic",
    color: "Red"
};

// Display the type of the car object in the console
console.log(typeof car);

// Modify the type property to Toyota and log the updated object
car.type = "Toyota";
console.log(car);

// Add a new property wheels with a value of 4 and log the updated object
car.wheels = 4;
console.log(car);